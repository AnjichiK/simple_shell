#include "shell.h"

/**
 * INTERACTIVE – Should Return T for shell in INTERACTIVE mode
 *
 * Return: 1 for INTERACTIVE mode, else 0
 */
int interactive(info_t *info)
{
	return (isatty(STDIN_FILENO) && info->readfd <= 2);
}

/**
 * is_delim – To check if CHAR is DELIMETER
 * @c: CHAR for checking
 * @delim: DELIMETER string
 * Return: 1 for T, 0 for F
 */
int is_delim(char c, char *delim)
{
	while (*delim)
		if (*delim++ == c)
			return (1);
	return (0);
}

/**
 * _isalpha – To check if alphabetic CHAR
 * @c: CHAR to enter
 * Return: 1 for alphabetic CHAR, else 0
 */

int _isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}

/**
 * _atoi - Changes string to INT
 * @s: String to change
 * Return: 0 for numberless string, else CHANGED NO.
 */

int _atoi(char *s)
{
	int i, sign = 1, flag = 0, output;
	unsigned int result = 0;

	for (i = 0; s[i] != '\0' && flag != 2; i++)
	{
		if (s[i] == '-')
			sign *= -1;

		if (s[i] >= '0' && s[i] <= '9')
		{
			flag = 1;
			result *= 10;
			result += (s[i] - '0');
		}
		else if (flag == 1)
			flag = 2;
	}

	if (sign == -1)
		output = -result;
	else
		output = result;

	return (output);
}

